const path = require("path");
const { execSync } = require("child_process");

const esbuild = require("esbuild");

function readGitShortSha({ cwd }) {
  try {
    return execSync("git rev-parse --short HEAD", { cwd, stdio: ["ignore", "pipe", "ignore"] })
      .toString()
      .trim();
  } catch (e) {
    return "";
  }
}

function buildSignature() {
  const sha = readGitShortSha({ cwd: path.resolve(__dirname, "..") });
  const date = new Date().toISOString().slice(0, 10);
  if (sha) return `${sha}-${date}`;
  return `dev-${date}`;
}

async function main() {
  const signature = buildSignature();

  const entry = path.resolve(__dirname, "../telephony/public/js/softphone/index.js");
  const outfile = path.resolve(__dirname, "../telephony/public/js/sip_softphone.js");

  console.log(`[telephony] building sip_softphone.js (${signature})`);

  await esbuild.build({
    entryPoints: [entry],
    outfile,
    bundle: true,
    format: "iife",
    target: ["es2019"],
    sourcemap: false,
    minify: false,
    banner: {
      js: `/* Generated by telephony/build/softphone.js (build: ${signature}) */`,
    },
    define: {
      __TP_SOFTPHONE_BUILD__: JSON.stringify(signature),
    },
  });
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
